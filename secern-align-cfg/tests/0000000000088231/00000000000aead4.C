#include<stdio.h>
int main()
{
    int t, k=0;
    scanf("%d", &t);
    while(t--)
    {
        int n, i=0, a, b;
        scanf("%d", &n);
        a=n/4;
        if(n%4)
            b=3*n/4+1;
        else
            b=3*n/4;
        if(a%10==4||b%10==4)
        {
            a=a+1;
            b=b-1;
        }
        else if((a/10)%10==4||(b/10)%10==4)
        {
            a=a+10;
            b=b-10;
        }
        else if((a/100)%10==4||(b/100)%10==4)
        {
            a=a+100;
            b=b-100;
        }
        else if((a/1000)%10==4||(b/1000)%10==4)
        {
            a=a+1000;
            b=b-1000;
        }
        else if((a/10000)%10==4||(b/10000)%10==4)
        {
            a=a+10000;
            b=b-10000;
        }
        else if((a/100000)%10==4||(b/100000)%10==4)
        {
            a=a+100000;
            b=b-100000;
        }
        else if((a/1000000)%10==4||(b/1000000)%10==4)
        {
            a=a+1000000;
            b=b-1000000;
        }
        else if((a/10000000)%10==4||(b/10000000)%10==4)
        {
            a=a+10000000;
            b=b-10000000;
        }
        else if((a/100000000)%10==4||(b/100000000)%10==4)
        {
            a=a+100000000;
            b=b-100000000;
        }
        else if((a/1000000000)%10==4||(b/1000000000)%10==4)
        {
            a=a+1000000000;
            b=b-1000000000;
        }
        else if((a/10000000000)%10==4||(b/10000000000)%10==4)
        {
            a=a+10000000000;
            b=b-10000000000;
        }
        printf("case #%d: %d %d\n", ++i, a, b);
    }
    return 0;
}
